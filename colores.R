# Mostrar paletas de colores
# Mostrar paletas de colores disponibles en el paquete RColorBrewer
# Las paletas de colores están optimizadas para verse en monitores y con suficiente contraste
# para distinguir bien los polígonos en los mapas
require(RColorBrewer)
display.brewer.pal(5,"PuRd")
display.brewer.pal(5,"Purples")
display.brewer.pal(5,"RdPu")
display.brewer.pal(5,"Reds")
